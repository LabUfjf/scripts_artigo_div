function [I,f] = PDF_integral(sg,xi,xs,type)
%==========================================================================
% Objetivo: Calcular Integral Analítica
%==========================================================================
syms x f
%%=========================================================================
%% Uniforme
%%=========================================================================
if strcmp(type,'Uniform')
    if xs>sg.g1.s
        xs=sg.g1.s;
    end
    if xi<sg.g1.i
        xi=sg.g1.i;
    end
    I=(1/(sg.g1.s-sg.g1.i))*(xs-xi);
end
%%=========================================================================
%% Gaussian
%%=========================================================================
if strcmp(type,'Gaussian')
    f=(1/(sg.g1.std*sqrt(2*pi)))*exp((-(x-sg.g1.mu)^2)/(2*sg.g1.std^2));
    I=double(int(f, xi, xs));
end
%%=========================================================================
%% Bimodal
%%=========================================================================
if strcmp(type,'Bimodal')
    f=[(1/(sg.g1.std*sqrt(2*pi)))*exp((-(x-sg.g1.mu)^2)/(2*sg.g1.std^2))+(1/(sg.g2.std*sqrt(2*pi)))*exp((-(x-sg.g2.mu)^2)/(2*sg.g2.std^2))]/2;
    I=double(int(f, xi, xs));
end
%%=========================================================================
%% Rayleigh
%%=========================================================================
if strcmp(type,'Rayleigh')
    f = (x/(sg.b^2))*exp((-x^2)/(2*sg.b^2));
    I=double(int(f, xi, xs));
end
%%=========================================================================
%% LogNormal
%%=========================================================================
if strcmp(type,'Logn')
    f=(1/(x*sg.std*sqrt(2*pi)))*exp(-((log(x)-sg.mu)^2)/(2*sg.std^2));
    I=double(int(f, xi, xs));
end
%%=========================================================================
%% Gamma
%%=========================================================================
if strcmp(type,'Gamma')
    f=(1/((sg.B^sg.A)*gamma(sg.A)))*(x^(sg.A-1))*exp((-x)/sg.B);
    I=double(int(f, xi, xs));
end


end
